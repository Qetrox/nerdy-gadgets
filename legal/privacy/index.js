
function shownoodzakelijk() {
    div = document.getElementById("noodzakelijk"); // Zoek de div met id 'noodzakelijk'
    if(div.style.display == 'none') { //checked of dat de display van de tekst none is
        div.style.display = 'block'; //als dat waar is maakt het de tekst zichtbaar
        div.style.background = '#333333'
        div.style.marginTop = '-7.4%'
        div.style.marginLeft = '3.4%'
    }
    else{
        div.style.display = 'none' //als display = none  NIET waar is maakt het de tekst ONTzichtbaar
    }

}

